/**
 * Multiplies two numbers
 * @param {number} a 
 * @param {number} b
 * @returns {number}
 */
export function multiply(a, b) {
  return a * b;
}

/**
 * Calculates power
 * @param {number} base
 * @param {number} exp
 * @returns {number}  
 */
export function power(base, exp) {
  return Math.pow(base, exp);
}
